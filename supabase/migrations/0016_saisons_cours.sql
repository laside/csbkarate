-- Cours de la saison 2026-2027 (n'insère que si la saison n'a pas déjà de cours).
insert into public.cours (saison_id, cours_type, libelle, jours, heures, lieu, accent, prix, position)
select s.id, v.cours_type, v.libelle, v.jours, v.heures, v.lieu, v.accent, v.prix, v.position
from public.saisons s
cross join (values
    ('Baby',         'Baby Karaté',           'Mardi et Jeudi',    '18h30 - 19h30', 'Gymnase Auguste Delaune', 'dojo',   18300, 0),
    ('Enfant',       'Enfants',               'Lundi et Vendredi', '18h00 - 19h00', 'École Louise Michel',     'dojo',   20300, 1),
    ('Adulte',       'Ados / Adultes',        'Mardi et Jeudi',    '19h30 - 21h00', 'Gymnase Auguste Delaune', 'dojo',   23300, 2),
    ('Self-Defense', 'Self-Défense Féminine', 'Dimanche',          '11h00 - 12h30', 'Gymnase Auguste Delaune', 'corail', 13300, 3)
) as v(cours_type, libelle, jours, heures, lieu, accent, prix, position)
where s.label = '2026-2027'
  and not exists (select 1 from public.cours c where c.saison_id = s.id);
